<script>
// JavaScript encapsulado para la sección de instalaciones
(function() {
    'use strict';
    
    // Variables para la galería
    let currentImageIndex = 0;
    let facilitiesImages = [];
    
    function initFacilitiesSection() {
        // Intersection Observer para animaciones
        const observerOptions = {
            threshold: 0.1,
            rootMargin: '0px 0px -50px 0px'
        };

        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    entry.target.style.opacity = '1';
                    if (entry.target.classList.contains('facilities-animate')) {
                        entry.target.style.transform = 'translateX(0)';
                    } else if (entry.target.classList.contains('facilities-gallery-animate')) {
                        entry.target.style.transform = 'translateX(0)';
                    }
                }
            });
        }, observerOptions);

        // Observar elementos animados
        const animatedElements = document.querySelectorAll('.facilities-animate, .facilities-gallery-animate');
        animatedElements.forEach(el => {
            observer.observe(el);
        });

        // Preparar datos de imágenes para el modal
        @if($facilitiesImages && $facilitiesImages->count() > 0)
        facilitiesImages = [
            @foreach($facilitiesImages as $image)
            {
                src: "{{ asset('storage/' . $image->image) }}",
                title: "{{ $image->title }}",
                description: "{{ $image->description ?? 'Vista de nuestras instalaciones' }}"
            },
            @endforeach
        ];
        @endif

        console.log('Sección de instalaciones inicializada correctamente');
    }

    // Función para abrir modal de imagen
    window.openModal = function(imageSrc, title, description) {
        // Crear modal dinámico
        const modal = document.createElement('div');
        modal.id = 'facilitiesModal';
        modal.className = 'fixed inset-0 bg-black bg-opacity-75 z-50 flex items-center justify-center p-4';
        modal.innerHTML = `
            <div class="max-w-4xl w-full max-h-[90vh] bg-white rounded-2xl overflow-hidden shadow-2xl">
                <div class="relative">
                    <img src="${imageSrc}" alt="${title}" class="w-full h-96 object-cover">
                    <button onclick="closeFacilitiesModal()" class="absolute top-4 right-4 w-8 h-8 bg-black bg-opacity-50 rounded-full flex items-center justify-center text-white hover:bg-opacity-75 transition-all">
                        <svg class="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                            <path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M6 18L18 6M6 6l12 12"></path>
                        </svg>
                    </button>
                </div>
                <div class="p-6">
                    <h3 class="text-2xl font-bold text-gray-900 mb-2">${title}</h3>
                    <p class="text-gray-600">${description}</p>
                </div>
            </div>
        `;
        
        document.body.appendChild(modal);
        
        // Animar apertura
        setTimeout(() => {
            modal.style.opacity = '1';
        }, 10);
        
        // Cerrar con click fuera
        modal.addEventListener('click', function(e) {
            if (e.target === modal) {
                closeFacilitiesModal();
            }
        });
    };

    // Función para cerrar modal
    window.closeFacilitiesModal = function() {
        const modal = document.getElementById('facilitiesModal');
        if (modal) {
            modal.style.opacity = '0';
            setTimeout(() => {
                modal.remove();
            }, 300);
        }
    };

    // Función para ver toda la galería
    window.viewAllGallery = function() {
        console.log('Ver galería completa - funcionalidad por implementar');
    };

    // Cerrar modal con Escape
    document.addEventListener('keydown', function(e) {
        if (e.key === 'Escape') {
            closeFacilitiesModal();
        }
    });

    // Inicializar cuando esté listo
    if (document.readyState === 'loading') {
        document.addEventListener('DOMContentLoaded', initFacilitiesSection);
    } else {
        initFacilitiesSection();
    }
})();
</script>